    investigation_conducted_en VARCHAR(10) COMMENT 'Investigation Conducted',
    investigation_details TEXT COMMENT '调查情况',
    investigation_details_en TEXT COMMENT 'Investigation Details',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
