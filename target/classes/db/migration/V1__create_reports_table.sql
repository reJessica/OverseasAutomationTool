    product_name VARCHAR(255) COMMENT '产品名称',
    product_name_en VARCHAR(255) COMMENT 'Product Name',
    registration_number VARCHAR(100) COMMENT '注册证编号',
    registration_number_en VARCHAR(100) COMMENT 'Registration Number',
